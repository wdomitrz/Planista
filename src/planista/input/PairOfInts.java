package planista.input;

public class PairOfInts {

    private final int first;
    private final int second;

    public PairOfInts(int first, int second) {
        this.first = first;
        this.second = second;
    }

    public int getFirst() {
        return first;
    }

    public int getSecond() {
        return second;
    }

}
